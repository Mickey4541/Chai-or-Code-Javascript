//>>>>>>>>>while loop>>>>>>>>>.......
let index = 0
while (index <= 5){
    console.log(`The value of index is : ${index}`);
    index++;
}

//>>>>>>>>>>>>>>>>>>>playing with array in while loop>>>>>>>>>>>>
let colors = ['red', 'blue', 'green', 'yellow', 'purple', 'orange']
let arr = 0
while(arr < colors.length){
    console.log(`value is ${colors[arr]}`);
    arr++;
}

//>>>>>>>>>>>>>>>>do while loop>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
let score = 1
do {
    console.log(`score is : ${score}`);
    score++;
} while (score <= 10);